class Product {
  final int id;
  final String name;
  final double price;
  final String img;
  final int category;
  final String description;
  final int duraction;
  const Product({
    required this.id,
    required this.name,
    required this.price,
    required this.img,
    required this.category,
    required this.duraction,
    required this.description,
  });
}
